public class StringExercise{
    public static void main(String[] args) {

        String var1 = "We don't need no education \nWe don't need no thought control";
        System.out.println(var1);

        String var2 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(var2);

        String var3 = "In windows, the main drive is usually C:\\";
        System.out.println(var3);

        String var4 = "I can do backslashes \\, double \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(var4);
    }
}
